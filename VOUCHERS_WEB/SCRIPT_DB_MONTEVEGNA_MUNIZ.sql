--SP PARA SABER SI ESTA REGISTRADO EL CLIENTE
CREATE PROCEDURE SP_VERIFICAR_CLIENTE
(
@DNI INT
)
AS
BEGIN

DECLARE @BIT BIT

SELECT @BIT=0

IF CAST((SELECT DNI FROM CLIENTES WHERE @DNI=DNI) AS BIT )=1
BEGIN
SELECT @BIT=1
END

RETURN @BIT

END

GO
