CREATE PROCEDURE SP_LISTAR_PROD_WIN_X_VOUCHER
(
@ID_VOUCHER VARCHAR(8)
)
AS
BEGIN

SELECT P.ID,P.NOMBRE,P.URLIMAGEN FROM PRODUCTOS AS P 
