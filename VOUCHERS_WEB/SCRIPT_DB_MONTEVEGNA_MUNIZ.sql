NOMBRE VARCHAR(30) NOT NULL,
APELLIDO VARCHAR(30) NOT NULL,
NROCALLE SMALLINT NOT NULL CHECK(NROCALLE>0),
CALLE VARCHAR(30) NOT NULL,
LOCALIDAD varchar(50) NOT NULL,
